    function validateForm() {
      var name = document.getElementById('name').value;
      var email = document.getElementById('email').value;
      var mobile = document.getElementById('number').value;
      var type = document.getElementById('Dtype').value;

      // Reset error messages
      resetErrors();

      // Flag to check if the form is valid
      var isValid = true;

      // Validate name (string only)
      if (!/^[a-zA-Z\s]+$/.test(name)) {
        displayError('name', 'Name should contain only letters and spaces.');
        isValid = false;
      }

      // Validate email format
      if (!isValidEmail(email)) {
        displayError('email', 'Invalid email address.');
        isValid = false;
      }

      // Validate mobile (integer, min 10, max 12 digits)
      if (!/^\d{10,12}$/.test(mobile)) {
        displayError('number', 'Mobile should be a number with 10 to 12 digits.');
        isValid = false;
      }

      // Validate type (select type)
      if (type === '') {
        displayError('Dtype', 'Please select a type.');
        isValid = false;
      }

      return isValid;
    }

    function isValidEmail(email) {
      // Simple email validation regex
      var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
      return emailRegex.test(email);
    }

    function displayError(fieldId, errorMessage) {
      var field = document.getElementById(fieldId);
      var errorDiv = field.nextElementSibling; // Assuming the error div is the next element

      errorDiv.innerHTML = errorMessage;
      field.classList.add('error');
    }

    function resetErrors() {
      var errorFields = document.querySelectorAll('.error');
      errorFields.forEach(function (field) {
        field.classList.remove('error');
      });

      var errorMessages = document.querySelectorAll('.help-block');
      errorMessages.forEach(function (message) {
        message.innerHTML = '';
      });
    }
  });
